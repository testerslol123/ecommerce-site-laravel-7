            $.fn.submitReview = function (callback) {
                var postData = $(this).serialize();
                var apiUrl = 'https://productreview-app.sirclo.com/api/review';
                var delay = 0;
                $.post(apiUrl, postData, function(data) {
                    setTimeout(function () {
                        callback(data);
                        
                    }, delay);
                })
                .fail(function(xhr, status, error) {

                    var err = JSON.parse(xhr.responseText);
                    var alertmsg = "";
                    
                    if(err.message == "Review content is at least 20 characters!"){
                        alertmsg = 'Review produk minimal 20 karakter.';
                    }else if(err.message == "Session invalid or not from a logged in user"){
                        alertmsg = 'Anda perlu login untuk memberikan rating dan review produk.';
                    }else if(err.message == "Review already exists!"){
                        alertmsg = 'Anda telah menulis review sebelumnya.';
                    }else if(err.message == "Rating value must be between 1 and 5 (inclusive)!"){
                        alertmsg = 'Rating harus bernilai 1 hingga 5!';
                    }
                    
                    $('#body-review-status').html('<div class="alert alert-'+status+'">\n\
                    <button type="button" class="close" data-dismiss="alert">×</button>\n\
                    '+alertmsg+'.</div>');
                });
            };

